/** Return the code generator init value for the 1st LFSR.
 * \param mesid ME signal ID
 * \param index GPS L2CL specific starting point index.
 *              For other signals any value can be written.
 * \return LFSR initial value
 */
u32 mesid_to_lfsr0_init(const me_gnss_signal_t mesid, const u8 index) {
  u32 ret = ~0;

  assert(mesid_valid(mesid));

  switch ((s8)mesid.code) {
    case CODE_GPS_L1CA:
    case CODE_AUX_GPS:
    case CODE_SBAS_L1CA:
    case CODE_QZS_L1CA:
      ret = 0x3FF;
      break;
    case CODE_GPS_L2CM:
      ret = gps_l2cm_prns_init_values[mesid.sat - GPS_FIRST_PRN] & 0x7FFFFFF;
      break;
    case CODE_QZS_L2CM:
      ret = qzss_l2cm_prns_init_values[mesid.sat - QZS_FIRST_PRN] & 0x7FFFFFF;
      break;
    case CODE_GPS_L2CL:
      ret = gps_l2cl_prns_init_values[mesid.sat - GPS_FIRST_PRN][index] &
            0x7FFFFFF;
      break;
    case CODE_QZS_L2CL:
      ret = qzss_l2cl_prns_init_values[mesid.sat - QZS_FIRST_PRN][index] &
            0x7FFFFFF;
      break;
    case CODE_GLO_L1OF:
    case CODE_GLO_L2OF:
      ret = 0x1FF;
      break;
    case CODE_BDS2_B1:
    case CODE_BDS2_B2:
      ret = 0x2AA;
      break;
    case CODE_BDS3_B5I:
      ret = bds3_b2a_prns_init_values[mesid.sat - BDS_FIRST_PRN] & 0x1FFF;
      break;
    case CODE_GPS_L5I:
      ret = gps_l5i_prns_init_values[mesid.sat - GPS_FIRST_PRN] & 0x1FFF;
      break;
    case CODE_GAL_E5I:
      ret = gal_e5i_prns_init_values[mesid.sat - GAL_FIRST_PRN] & 0x3FFF;
      break;
    case CODE_GAL_E7I:
      ret = gal_e7i_prns_init_values[mesid.sat - GAL_FIRST_PRN] & 0x3FFF;
      break;
    default:
      assert(!"Unsupported signal code ID");
      break;
  }

  return ret;
}

