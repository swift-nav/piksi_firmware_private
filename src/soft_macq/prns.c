/** Returns the C/A code PRN corresponding to a given mesid. The N chip
 * PRN is packed one chip per bit into an array of ceil(N/8) 8-bit words.
 * The final 8*ceil(N/8) bit are "Do Not Care" for all PRNs.
 *
 * \param mesid ME signal ID.
 *
 * \return A pointer to an array containing the PRN code corresponding to
 *         the PRN number <code>prn</code>, packed one bit per chip.
 */
const u8* ca_code(const me_gnss_signal_t mesid) {
  assert(mesid_valid(mesid));

  if (IS_GLO(mesid)) {
    return glo_ca_codes;
  }

  if (IS_BDS2(mesid)) {
    return bds2_codes[(mesid.sat) - BDS_FIRST_PRN];
  }

  if ((CODE_GAL_E1B == mesid.code) || (CODE_GAL_E1X == mesid.code)) {
    return gal_e1b_codes[(mesid.sat) - GAL_FIRST_PRN];
  }

  if (CODE_GAL_E1C == mesid.code) {
    return gal_e1c_codes[(mesid.sat) - GAL_FIRST_PRN];
  }

  const prn_array_t prn_array = prn_array_table[mesid.code];
  if (prn_array == NULL) {
    log_error("Empty code table for value %d", mesid.code);
    assert(0);
    return NULL;
  }
  return prn_array[mesid_to_code_index(mesid)];
}

inline s8 get_chip(const u8* const code, const u32 chip_num) {
  u32 byte = chip_num >> 3;
  u8 bit = 7 - (chip_num & 0x7);

  return ((code[byte] >> bit) & 1) ? -1 : 1;
}

